    //press button to remove employee to database and refresh data on gui
    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        // TODO add your handling code here:
        try {
                EmployeeDB emp = DatabaseHandler.findEmployeeById(new DatabaseHandler(), Integer.parseInt(inputRemoveEmp.getText()));
                if (emp.getEmployeeLevel() < 3)
                {
                DatabaseHandler.removeEmployee(new DatabaseHandler(), emp);
                } else {
                    JOptionPane.showMessageDialog(null, "Can't Delete Manager", "Error", JOptionPane.WARNING_MESSAGE);
                }
                empGen();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_removeButtonActionPerformed

