    //press button to add employee to database and refresh data on gui
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        try {
            EmployeeDB emp = new EmployeeDB();
            boolean isClickedAll = clicked[0] && clicked[1] && clicked[2] && clicked[3] && clicked[4] && clicked[5] && clicked[6];
            if (!isClickedAll) 
            {
                JOptionPane.showMessageDialog(null, "Please Enter All Data", "Error", JOptionPane.WARNING_MESSAGE);
            }
            else if (DatabaseHandler.findEmployeeById(new DatabaseHandler(), Integer.parseInt(inputEmp.getText())) == null)
            {
            emp.setEmployeeID(Integer.parseInt(inputEmp.getText()));
            emp.setFirstName(inputFirstName.getText());
            emp.setLastName(inputLastname.getText());
            emp.setPasswordEmp(inputPassword.getText());
            emp.setAge(Integer.parseInt(inputAge.getText()));
            emp.setAddress(inputAddress.getText());
            emp.setPhoneNumber(InputPhone.getText());
            emp.setWorkStatus(InputWork.getText());
            emp.setEmployeeLevel(jComboBox1.getSelectedIndex()+1);
            DatabaseHandler.insertEmployee(new DatabaseHandler(), emp);
            empGen();
            } else {
                JOptionPane.showMessageDialog(null, "ID Already Exist", "Error", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_addButtonActionPerformed

