    //Show employee on gui
    public void empGen() {
        DefaultTableModel tableModel = (DefaultTableModel) showEmpTable.getModel();
        tableModel.setRowCount(0);
        try {
            ArrayList<EmployeeDB> employeeDBs = DatabaseHandler.findAllEmployee(new DatabaseHandler());
            for (int i = 0; i < employeeDBs.size(); i++) {
                tableModel.addRow(new Object[]{employeeDBs.get(i).getEmployeeID(),
                    employeeDBs.get(i).getFirstName(),
                    employeeDBs.get(i).getLastName(),
                    employeeDBs.get(i).getPhoneNumber(),
                    employeeDBs.get(i).getAddress(),
                    employeeDBs.get(i).getEmployeeLevel()});
            }
        } catch (Exception e) {
            System.err.println("Error!");
        }
    }

